package josesk.javameengine.sound;

import javax.microedition.media.Player;
import javax.microedition.media.PlayerListener;

import josesk.javameengine.Sound;

public class AutoDestructiveSound extends Sound{
	//Sound that closes it-self after playing
	//Very useful in J2ME
	
	/**
	 * Sound class that will close automatically after playing itself. Useful for playing once or rarely played SFX to save RAM and audio buffering
	 * @param String	source
	 * @throws Error
	 * @throws Exception
	 */
	public AutoDestructiveSound(String source) throws Error, Exception {
		super(source, false);
	}
	
	protected void onPlayerUpdate(Player player, String event, Object eventData) {
		
		if(event.equals(PlayerListener.END_OF_MEDIA) || event.equals(PlayerListener.STOPPED)) {
			
			try {
				Close();
			}catch(Exception e) {
			}catch(Error e) {}
			
		}
		
	}

}
