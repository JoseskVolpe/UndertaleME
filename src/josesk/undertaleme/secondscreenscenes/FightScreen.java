package josesk.undertaleme.secondscreenscenes;

import javax.microedition.lcdui.Graphics;
import javax.microedition.lcdui.game.Sprite;

import josesk.undertaleme.GameEngine;
import josesk.undertaleme.GameView;
import josesk.undertaleme.layout.Player;

/**
 * Fight scene default secondary screen
 * @author JoseskVolpe
 *
 */
public class FightScreen extends GameView{
	
	Player originalplayer;
	Sprite player;
	
	public FightScreen(Player player) {
		
		originalplayer = player;
		this.player = new Sprite(player);
		
	}
	
	public void Update() {
		
		player.setPosition((GameEngine.getCanvas().getWidth()/2)-4, (GameEngine.getCanvas().getHeight()/2)-4);
		
	}
	
	public void render(Graphics g) {
		
		g.setColor(0x000000);
		g.fillRect(0, 0, GameEngine.getCanvas().getWidth(), GameEngine.getCanvas().getHeight());
		g.fillRect(0, 0, 1, 1); //Glitch prevent
		
		if(player!=null&&originalplayer!=null) {
			player.setFrame(originalplayer.getFrame());
			player.setVisible(originalplayer.isVisible());
			player.paint(g);
		}
		
	}
	
	public void onGameViewDestroy() {
		
		player = null;
		originalplayer = null;
		
	}
	
	public boolean onError(String error) {
		
		player=null;
		originalplayer=null;
		
		return true;
		
	}

}
