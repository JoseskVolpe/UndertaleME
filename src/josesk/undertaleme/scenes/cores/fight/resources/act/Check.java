package josesk.undertaleme.scenes.cores.fight.resources.act;

import josesk.javameengine.Text;
import josesk.undertaleme.layout.Dialog;
import josesk.undertaleme.scenes.cores.Fight;
import josesk.undertaleme.scenes.cores.fight.resources.Monster;
import josesk.undertaleme.scenes.cores.fight.resources.monsterresources.Act;

/**
 * Check action
 *
 */
public class Check extends Act{

	public Check(Monster monster) {
		super(monster);
		
		setName("Check");
	}
	
	public void select(Fight fightScene, Monster monster) throws Error, Exception {
		
		Text message = new Text(monster.getName().toUpperCase()+" - ATK "+monster.getAtk()+" DEF "+getMonster().getDef()+"\n*"+monster.getDescription(), "checkAct_"+monster.getName());
		
		fightScene.actMessage(message);
		
	}

}
