package josesk.undertaleme.scenes.cores.fight.resources.attackeffects;

import javax.microedition.lcdui.game.Sprite;

import josesk.javameengine.LoadResources;
import josesk.javameengine.sound.NonSyncSound;
import josesk.undertaleme.scenes.cores.fight.resources.AttackEffect;

/**
 * Knife default effect
 *
 */
public class KnifeEffect extends AttackEffect{

	private static Sprite effectSprite;
	
	protected void Load() throws Error, Exception {
		loadSound("/Attack.wav", 800);
		
		if(effectSprite==null) {
			
			effectSprite = LoadResources.createSprite("/AttackEffect.png", 18, 66);
			
		}
		
	}
	
	protected void unload() throws Error, Exception{
		effectSprite = null;
	}
	
	protected void onBegin() throws Error, Exception{
		
		startSprite(effectSprite);
		frame=0;
		
		if(sound!=null) {
			sound.Play();
		}
		
	}
	
	public void update() throws Error, Exception{
		x=(short) (monster.getX()+monster.getEffectX());
		y=(short) (monster.getY()+monster.getEffectY());
	}
	
	public void run() {
		for(byte i=1; i<=5; i++) {
			
			try {
				sync.syncSleep(70);
			} catch (InterruptedException e) {}
			frame=i;
			
		}
		
		try {
			sync.syncSleep(100);
		} catch (InterruptedException e) {}
		
		finishAnimation();
		
	}

}
